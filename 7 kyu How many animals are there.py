# How many animals are there?


def count_animals(sentence):
    return sentence.split()


print(count_animals("I see 3 zebras, 5 lions and 6 giraffes."), 'Live from the Savannah')
print(count_animals("Mom, 3 rhinoceros and 6 snakes come to us!"), 'Close the car !')
print(count_animals("I do not see any animals here!"), 'Live from the kitchen')
