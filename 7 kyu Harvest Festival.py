def plant(seed, water, fert, temp):
    s = ''

    if 20 <= temp <= 30:
        for _ in range(water):
            s += water * '-' + seed * fert
    else:
        s = f'{"-" * (water * water)}{seed}'

    return s


print(plant(",", 3, 7, 25), "---,,,,,,,---,,,,,,,---,,,,,,,")
print(plant("+", 1, 3, 15), "-+")
print(plant(";", 9, 10, 30),
      "---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;")
