def plant(seed, water, fert, temp):
    s = ''

    for _ in range(water):
        s += water * '-' + seed * fert

    return s


print(plant(",", 3, 7, 25), "---,,,,,,,---,,,,,,,---,,,,,,,")
print(plant("+", 1, 3, 15), "-+")
print(plant(";", 9, 10, 30),
      "---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;---------;;;;;;;;;;")
